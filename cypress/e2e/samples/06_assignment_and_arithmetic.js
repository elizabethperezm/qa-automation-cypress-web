// Assignment Operators
let x = 10;
console.log(x); // Output: 10

x += 5; // Equivalent to x = x + 5
console.log(x); // Output: 15

x *= 2; // Equivalent to x = x * 2
console.log(x); // Output: 30

// Arithmetic Operators
let a = 7, b = 3;

console.log(a + b); // Addition: Output: 10
console.log(a - b); // Subtraction: Output: 4
console.log(a * b); // Multiplication: Output: 21
console.log(a / b); // Division: Output: 2.3333333333333335
console.log(a % b); // Modulus: Output: 1
console.log(a ** b); // Exponentiation: Output: 343
